#!/bin/bash
# OEDON Color Palette & ASCII Icons

# Text Styles
BOLD='\033[1m'
NC='\033[0m' # No Color

# Base Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
PURPLE='\033[0;35m'
CYAN='\033[0;36m'

# ASCII Status Tags (Professional Look)
OK="${GREEN}[OK]${NC}"
ERR="${RED}[ERR]${NC}"
INFO="${CYAN}[INFO]${NC}"
SSL="${PURPLE}[SSL]${NC}"
WARN="${YELLOW}[WARN]${NC}"
